#ifndef TIME_UTIL_H
#define TIME_UTIL_H

#include <stdint.h>
#include <sys/time.h>

static inline uint64_t time_now_ms(void) {
    struct timeval tv;
    gettimeofday(&tv, NULL);
    return (uint64_t)tv.tv_sec * 1000 + tv.tv_usec / 1000;
}

#endif /* TIME_UTIL_H */
