set -eu

if [ "$#" -lt 2 ]; then
    echo "usage: $0 <logical-node-id> <run-dir>" >&2
    exit 2
fi

LOGICAL_NODE_ID="$1"
RUN_DIR="$2"

STATE_DIR="$RUN_DIR/runtime-nodes"
TIMEOUT_SEC="${ULAB_NODE_READY_TIMEOUT_SEC:-180}"
SLEEP_SEC="${ULAB_NODE_READY_SLEEP_SEC:-3}"
STARTER_PORT="${ULAB_STARTER_PORT:-18001}"

safe_name() {
    printf "%s" "$1" | tr -c 'A-Za-z0-9_.-' '-'
}

need_cmd() {
    if ! command -v "$1" >/dev/null 2>&1; then
        echo "missing required command: $1" >&2
        exit 1
    fi
}

load_state() {
    state_file="$STATE_DIR/$(safe_name "$LOGICAL_NODE_ID").env"

    if [ ! -f "$state_file" ]; then
        echo "node state not found for $LOGICAL_NODE_ID: $state_file" >&2
        exit 1
    fi

    # State file is generated by build-and-start-site.sh/runtime.c and contains
    # simple KEY=value lines.
    # shellcheck disable=SC1090
    . "$state_file"
}

container_running() {
    podman inspect -f '{{.State.Running}}' "$CONTAINER_NAME" 2>/dev/null |
        grep -q '^true$'
}

container_logs_tail() {
    podman logs --tail 80 "$CONTAINER_NAME" 2>/dev/null || true
}

starter_status() {
    podman exec "$CONTAINER_NAME" sh -lc \
        "curl -fsS --max-time 2 http://127.0.0.1:${STARTER_PORT}/v1/status" \
        2>/dev/null
}

starter_apps_running() {
    tmp_file="/tmp/ukama-starter-status.$$"

    if ! starter_status > "$tmp_file"; then
        rm -f "$tmp_file"
        return 1
    fi

    python3 - "$tmp_file" <<'PY'
import json
import os
import sys

path = sys.argv[1]

try:
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)
except Exception as exc:
    print(f"invalid starter.d JSON: {exc}", file=sys.stderr)
    sys.exit(1)

starterd = data.get("starterd") or {}
if starterd.get("updateInProgress") is True:
    print("starterd update in progress", file=sys.stderr)
    sys.exit(1)
if starterd.get("terminateRequested") is True:
    print("starterd terminate requested", file=sys.stderr)
    sys.exit(1)

apps = []
spaces = data.get("spaces")
if isinstance(spaces, list):
    for space in spaces:
        space_name = str(space.get("name") or "unknown")
        for app in space.get("apps") or []:
            if isinstance(app, dict):
                app = dict(app)
                app["_space"] = space_name
                apps.append(app)

if not apps:
    legacy = data.get("apps") or data.get("data", {}).get("apps") or data.get("services")
    if isinstance(legacy, list):
        apps = [app for app in legacy if isinstance(app, dict)]

if not apps:
    print("starter.d status has no apps list", file=sys.stderr)
    sys.exit(1)

required = [x.strip() for x in os.environ.get("ULAB_REQUIRED_STARTER_APPS", "").split(",") if x.strip()]
required_set = set(required)
seen = set()
bad = []

for app in apps:
    name = str(app.get("name") or app.get("tag") or "unknown")
    state = str(app.get("state") or app.get("status") or "").strip()
    normalized = state.lower()
    if not required_set or name in required_set:
        seen.add(name)
        if normalized not in ("running", "active"):
            bad.append((app.get("_space") or "apps", name, state))

missing = sorted(required_set - seen)
for name in missing:
    bad.append(("apps", name, "missing"))

if bad:
    for space, name, state in bad:
        print(f"{space}/{name}: {state}", file=sys.stderr)
    sys.exit(1)

sys.exit(0)
PY

    rc="$?"
    rm -f "$tmp_file"

    return "$rc"
}

print_starter_debug() {
    tmp_file="/tmp/ukama-starter-status-debug.$$"

    echo "---- starter.d /v1/status ----" >&2

    if starter_status > "$tmp_file"; then
        cat "$tmp_file" >&2
        echo >&2

        echo "---- starter.d non-running apps ----" >&2

        python3 - "$tmp_file" <<'PY' >&2 || true
import json
import sys

path = sys.argv[1]

try:
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)
except Exception:
    sys.exit(0)

apps = []
spaces = data.get("spaces")
if isinstance(spaces, list):
    for space in spaces:
        space_name = str(space.get("name") or "unknown")
        for app in space.get("apps") or []:
            if isinstance(app, dict):
                app = dict(app)
                app["_space"] = space_name
                apps.append(app)

if not apps:
    legacy = data.get("apps") or data.get("data", {}).get("apps") or data.get("services")
    if isinstance(legacy, list):
        apps = [app for app in legacy if isinstance(app, dict)]

found = False
for app in apps:
    name = str(app.get("name") or app.get("tag") or "unknown")
    state = str(app.get("state") or app.get("status") or "").strip()
    normalized = state.lower()
    if normalized not in ("running", "active"):
        found = True
        print(f"{app.get('_space') or 'apps'}/{name}: {state}")

if not found:
    print("none")
PY
    else
        echo "starter.d not reachable on port ${STARTER_PORT}" >&2
    fi

    rm -f "$tmp_file"
}

node_ready() {
    container_running || return 1
    starter_apps_running || return 1
    return 0
}

need_cmd podman
need_cmd python3
load_state

echo "wait-node: logical=$LOGICAL_NODE_ID factory=$FACTORY_NODE_ID container=$CONTAINER_NAME timeout=${TIMEOUT_SEC}s"

start_ts="$(date +%s)"

while :; do
    if node_ready; then
        echo "node-ready logical=$LOGICAL_NODE_ID factory=$FACTORY_NODE_ID container=$CONTAINER_NAME"
        exit 0
    fi

    now_ts="$(date +%s)"
    elapsed=$((now_ts - start_ts))

    if [ "$elapsed" -ge "$TIMEOUT_SEC" ]; then
        echo "node not ready after ${TIMEOUT_SEC}s: logical=$LOGICAL_NODE_ID factory=$FACTORY_NODE_ID container=$CONTAINER_NAME" >&2

        echo "---- podman ps ----" >&2
        podman ps -a --filter "name=$CONTAINER_NAME" >&2 || true

        print_starter_debug

        echo "---- container logs ----" >&2
        container_logs_tail >&2

        exit 1
    fi

    sleep "$SLEEP_SEC"
done
